package com.jnit.oca2017.methods;

public class Unboxing {

	public static void main(String[] args) {

		Integer i = new Integer(56);

		// the following lines is added by the compiler to do the
		// unboxing.Converting the Integer object to int primitive

		Integer y = i.intValue();

		System.out.println(y);

		int g = y + 7;// arithmatic operators can be applied
		System.out.println(g);

	}

}
